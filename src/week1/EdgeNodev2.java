package week1;

public class EdgeNodev2 {
	private int edgeCost;
	private int v;

	public EdgeNodev2(int v, int edgeCost) {
		this.v = v;
		this.edgeCost = edgeCost;

	}

	public int getEdgeCost() {
		return edgeCost;
	}

	public void setEdgeCost(int edgeCost) {
		this.edgeCost = edgeCost;
	}

	public int getV() {
		return v;
	}

	public void setV(int v) {
		this.v = v;
	}

}
